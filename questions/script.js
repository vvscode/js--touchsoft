const shuffleList = list => {
  for (let i = 0; i < list.length; i++) {
    const rand = Math.floor(Math.random() * list.length);
    const tmp = list[rand];
    list[rand] = list[list.length - 1];
    list[list.length - 1] = tmp;
  }
  return list;
};

const $$ = document.querySelector.bind(document);
const questionsStrings = [
  questions0,
  questions1,
  questions2,
  questions3,
  questions4,
  questions5
];
let roundQuestionsNumber = 0;
let i = 0;

const getQuestions = (() => {
  let questions = [];
  const generateQuestions = () => {
    [...document.querySelectorAll('fieldset input[type=checkbox]')].forEach(
      (el, index) => {
        if (el.checked) {
          questions = [
            ...questions,
            ...(questionsStrings[index] || '').trim().split('\n')
          ];
        }
      }
    );

    questions = questions.map(i => i.trim()).filter(Boolean);

    shuffleList(questions);
    shuffleList(questions);
    roundQuestionsNumber = questions.length;
    i = 0;
  };

  return () => {
    if (!questions.length) {
      generateQuestions();
    }
    return questions;
  };
})();

const drawNextQuestion = () => {
  const questions = getQuestions();
  i++;
  const question = questions.pop();
  $$(
    '.question'
  ).innerHTML = `<sup>${i}</sup>/<sub>${roundQuestionsNumber}</sub> > ${question}`;
};

$$('.question').addEventListener('click', drawNextQuestion);
document.body.addEventListener('keyup', ev => {
  const SPACE_KEY_CODE = 32;
  if (ev.key === ' ' || ev.keyCode === SPACE_KEY_CODE) {
    drawNextQuestion();
  }
});
